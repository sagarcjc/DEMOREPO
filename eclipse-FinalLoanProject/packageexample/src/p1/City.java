package p1;

public class City {
   private String cname;
   private int cpin;
   private String area;
public String getCname() {
	return cname;
}
public void setCname(String cname) {
	this.cname = cname;
}
public int getCpin() {
	return cpin;
}
public void setCpin(int cpin) {
	this.cpin = cpin;
}
public String getArea() {
	return area;
}
public void setArea(String area) {
	this.area = area;
}
   
}
